    ADD CONSTRAINT PK_PRO PRIMARY KEY(PRO_NO);
    
ALTER TABLE LECTURE
    ADD CONSTRAINT PK_LEC PRIMARY KEY(LEC_NO);
    
ALTER TABLE ENROLL
    ADD CONSTRAINT PK_ENROLL PRIMARY KEY (ENR_NO);

ALTER TABLE COURSE
    ADD CONSTRAINT PK_COURSE PRIMARY KEY(COU_NO);
    
