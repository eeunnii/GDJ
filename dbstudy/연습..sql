CREATE TABLE STUDENT(
    STU_NO NUMBER NOT NULL,
    STU_NAME VARCHAR2(5 BYTE) NOT NULL,
    STU_ADRESS VARCHAR2 NULL,
    STU_GRADE VARCHAR2 NOT NULL,
    STU_PRO_PN VARCHAR NULL
);

CREATE TABLE PROFESSOR(
    PRO_NO NUMBER NOT NULL,
    PRO_NAME VARCHAR NOT NULL,
    PRO_SPE VARCHAR NOT NULL
);   

CREATE TABLE LECTURE(
    LEC_NO NUMBER NOT NULL,
    PRO_NO NUMBER NOT NULL,
    ENR_NO NUMBER NOT NULL,
    LEC_NAME VARCHAR2 NOT NULL,
    LEC_PLC VARCHAR2 NOT NULL
);

CREATE TABLE ENROLL(
    ENR_NO NUMBER NOT NULL,
    STU_NO NUMBER NOT NULL,
    COU_NO NUMBER NOT NULL,
    ENR DATE NOT NULL
);

CREATE TABLE COURSE(
    COU_NO NUMBER NOT NULL,
    COU_NAME VARCHAR2 NOT NULL,
    COU_GRA NUMBER NULL
);

