package quiz04;

public class Regular extends Employee {

	private int salary;

	public Regular(int empNo, String name, int salary) {
		super(empNo, name);
		this.salary = salary;
	}
	
	
	
	

	@Override
	public String toString() {
		return "Regular [salary=" + salary + "]";
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
